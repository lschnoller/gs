			<h3>El Método Biodinámica Ayurveda -MBA-</h3>
			<h4>es un nuevo concepto en <strong>capacitación</strong> que combina la esencia del Ayurveda  </h4>
			<h4>y del Yoga con la contribución de modernos enfoques científicos.</h4>
			<div class="yui-g"> 
				<div class="yui-u first">
					<p>Trabajamos con herramientas específicas, como la meditación y el masaje holístico, que constituyen los principales pilares del MBA. Estas técnicas dinámicas y de <strong>autoconocimiento</strong> facilitan un proceso experimental valiosísimo.</p>
					<p>El MBA propone, mediante un aprendizaje integral, una forma innovadora de facilitar los procesos individuales y el <strong>desarrollo personal</strong>. Este enfoque también ofrece abordajes grupales, incluye cursos, seminarios y capacitación de instructores. </p>
				</div>
				<div class="yui-u">
					<p>Desde el MBA acompañamos a las personas para que a partir de la propia experiencia, puedan ir desplegando todo su <strong>potencial</strong>. Al ir relacionando los diferentes planos del ser: físico, emocional, intelectual, espiritual, las ayudamos a considerar todos sus aspectos.</p>
					<p>La transformación que van experimentando los practicantes mediante el MBA, es un proceso captado momento a momento, que conduce hacia la Fuente de la alegría y la <strong>abundancia</strong>.</p>
